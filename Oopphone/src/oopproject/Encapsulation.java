package oopproject;

public class Encapsulation {
	private String name;
	private int price;
	public String getname() {
		return name;
	}
	public void setname(String name) {
		this.name = name;
	}
	public int getprice() {
		return price;
	}
	public void setprice(int price) {
		this.price = price;
	}

}
